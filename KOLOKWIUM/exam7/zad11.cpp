// Typ pliku to kategoria lub format pliku, które są zwykle określane przez rozszerzenie pliku, czyli ciąg znaków dodany do końca nazwy 
// pliku. Rozszerzenie pliku zazwyczaj zaczyna się od kropki (np. ".txt", ".doc", ".jpg").

// Różne typy plików są używane do różnych celów. Oto kilka przykładów typów plików:

// .txt: To jest prosty plik tekstowy, który zawiera tylko tekst bez żadnych specjalnych formatowań lub grafik. Pliki .txt mogą być 
// odczytywane przez większość programów do edycji tekstu.

// .doc / .docx: To są formaty plików używane przez program Microsoft Word. Pliki te mogą zawierać sformatowany tekst, obrazy, tabele, a 
// nawet zagnieżdżone obiekty, takie jak wykresy.

// .jpg / .png / .gif: To są formaty plików obrazowych. Każdy z nich ma swoje unikalne cechy; na przykład, .jpg jest zwykle używany do zdjęć,
//  .png obsługuje przezroczystość, a .gif umożliwia animacje.

// .mp3 / .wav / .flac: To są formaty plików audio. Podobnie jak w przypadku plików obrazowych, różne formaty audio mają różne cechy; na 
// przykład, .mp3 jest zwykle używany do muzyki z powodu swojej dobrej jakości dźwięku i niskiego zużycia pamięci, podczas gdy .wav i .flac 
// oferują dźwięk w wyższej jakości, ale zajmują więcej miejsca.

// .cpp / .py / .java: To są przykładowe rozszerzenia dla plików zawierających kod źródłowy w różnych językach programowania: C++, Python 
// i Java.

