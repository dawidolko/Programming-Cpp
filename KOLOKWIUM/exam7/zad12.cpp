// W językach programowania, zmienna to miejsce w pamięci komputera, które przechowuje wartość. Typ zmiennej określa 
// rodzaj danych, które może przechowywać, a także ilość miejsca, które zajmuje w pamięci.

// W C++, które jest językiem programowania o statycznym typowaniu, typ zmiennej musi być określony podczas deklaracji 
// zmiennej. Oto niektóre podstawowe typy zmiennych w C++:

// Typy liczbowe:

// int: służy do przechowywania liczb całkowitych.
// float: służy do przechowywania liczb zmiennoprzecinkowych z precyzją pojedynczą.
// double: służy do przechowywania liczb zmiennoprzecinkowych z precyzją podwójną.
// short i long: są używane do przechowywania liczb całkowitych, ale z różnym zakresem wartości.
// unsigned: jest modyfikatorem, który może być stosowany do typów całkowitych, oznacza, że zmienna może przechowywać 
// tylko wartości nieujemne.
// Typy znakowe:

// char: służy do przechowywania pojedynczych znaków.
// Typy logiczne:

// bool: służy do przechowywania wartości logicznych true lub false.
// Typy złożone:

// string: służy do przechowywania ciągów znaków.
// array: służy do przechowywania wielu wartości tego samego typu.
// struct, class: służą do definiowania typów złożonych, które mogą zawierać wiele pól różnych typów.
// Warto zauważyć, że dostępne typy zmiennych mogą różnić się w zależności od języka programowania. Na przykład, języki
//  takie jak Python oferują bardziej złożone wbudowane typy, takie jak listy i słowniki, a niektóre języki, takie jak 
//  JavaScript, mają dynamiczne typowanie, co oznacza, że typ zmiennej może zmieniać się w trakcie działania programu.